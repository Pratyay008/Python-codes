# 4. Write a short program to input a digit and print it in words.
a=int(input("Enter a digit (0-4) : "))
if a==0:
    print("The number is zero")
elif a==1:
    print("The number is one")
elif a==2:
    print("The number is two")
elif a==3:
    print("The number is three")
elif a==4:
    print("The number is four")
else:
    print("Invalid digit")
        